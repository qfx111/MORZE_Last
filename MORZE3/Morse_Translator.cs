        public static void Translate(int i)
        {
            var instr = "";
            using (var fReader = new StreamReader("./read.txt"))
            {
                while (!fReader.EndOfStream)
                    instr += Convert.ToChar(fReader.Read());
            }

            Console.WriteLine(instr);
            var outS = "";
            while (instr.Length > 0)
            {
                var j = 0;
                var x = "";
                while (j < instr.Length && instr[j] != ' ')
                {
                    x += instr[j];
                    j++;
                }

                if (instr[0] == ' ') x += "1";
                instr = instr.Remove(0, x.Length);
                if (x == "1")
                    outS += " ";
                else
                    switch (i)
                    {
                        case 1:
                            {
                                try
                                {
                                    outS += toEng[x];
                                }
                                catch
                                {
                                    outS += x;
                                }

                                break;
                            }
                        case 2:
                            try
                            {
                                outS += fromEng[x];
                            }
                            catch
                            {
                                outS += x;
                            }

                            break;
                        case 3:
                            try
                            {
                                outS += toRus[x];
                            }
                            catch
                            {
                                outS += x;
                            }

                            break;
                        case 4:
                            try
                            {
                                outS += fromRus[x];
                            }
                            catch
                            {
                                outS += x;
                            }


                            break;
                    }
            }

            using (var fWriter = new StreamWriter("./output.txt", false))
            {
                fWriter.Write(outS);
            }
        }
